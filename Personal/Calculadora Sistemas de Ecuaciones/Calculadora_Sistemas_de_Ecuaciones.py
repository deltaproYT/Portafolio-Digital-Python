def roundnum(varlist):
    newvlist = []
    for num in varlist:
        if num == int(num):  # Si el número es igual a su versión entera
            newvlist.append(int(num))
        else:
            newvlist.append(round(num, 3))  # Redondea a 3 decimales
    return newvlist



        


